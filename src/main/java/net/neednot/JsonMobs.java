package net.neednot;

public class JsonMobs {

    public String mob;
    public String name;
    public int kills;
    public int deaths;

    public void setMob(String mob) {
        this.mob = mob;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void  setKills(int kills) {
        this.kills = kills;
    }
    public void setDeaths(int deaths) {
        this.deaths = deaths;
    }
}